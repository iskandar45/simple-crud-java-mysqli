    private void load_table() {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama");
        model.addColumn("NIM");
        model.addColumn("Jurusan");
        model.addColumn("Alamat");
        model.addColumn("Phone");

        //menampilkan data database kedalam tabel
        try {
            int no = 1;
            String sql = "select * from mhs";
            java.sql.Connection conn = (Connection) config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{no++, res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5)});
            }
            jTable1.setModel(model);
        } catch (Exception e) {
        }

        // desain tamilan untuk combobox
        jComboBox1.addItem("teknik Informatika");
        jComboBox1.addItem("teknik Sipil");
        jComboBox1.addItem("teknik Kimia");
    }

